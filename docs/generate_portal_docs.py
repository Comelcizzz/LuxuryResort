def clean_inline(text: str) -> str:
    text = text.strip()
    text = re.sub(r"`([^`]+)`", r"\1", text)
    text = html.escape(text)
    text = text.replace("&amp;nbsp;", " ")
    return text


def markdown_to_story(markdown: str, styles) -> list:
    story: list = []
    title_done = False
    bullet_items: list[ListItem] = []

    def flush_bullets():
        nonlocal bullet_items
        if bullet_items:
            story.append(
                ListFlowable(
                    bullet_items,
                    bulletType="bullet",
                    leftIndent=14,
                    bulletFontName=styles["body"].fontName,
                    bulletFontSize=8,
                    bulletColor=colors.HexColor("#0f172a"),
                    spaceAfter=5,
                )
            )
            bullet_items = []

    for raw in markdown.splitlines():
        line = raw.rstrip()
        if not line.strip():
            flush_bullets()
            story.append(Spacer(1, 3))
            continue

        if line.startswith("# "):
            flush_bullets()
            if title_done:
                story.append(PageBreak())
            story.append(Paragraph(clean_inline(line[2:]), styles["title"]))
            title_done = True
            continue

        if line.startswith("## "):
            flush_bullets()
            story.append(Paragraph(clean_inline(line[3:]), styles["h2"]))
            continue

        if line.startswith("### "):
            flush_bullets()
            story.append(Paragraph(clean_inline(line[4:]), styles["h3"]))
            continue

        bullet_match = re.match(r"^\s*[-*]\s+(.+)$", line)
        if bullet_match:
            bullet_items.append(ListItem(Paragraph(clean_inline(bullet_match.group(1)), styles["bullet"])))
            continue

        numbered_match = re.match(r"^\s*(\d+)\.\s+(.+)$", line)
        if numbered_match:
            bullet_items.append(ListItem(Paragraph(clean_inline(f"{numbered_match.group(1)}. {numbered_match.group(2)}"), styles["bullet"])))
            continue

        flush_bullets()
        story.append(Paragraph(clean_inline(line), styles["body"]))

    flush_bullets()
    return story


def footer(canvas, doc):
    canvas.saveState()
    canvas.setFont("ArialUnicode", 8)
    canvas.setFillColor(colors.HexColor("#64748b"))
    canvas.drawRightString(A4[0] - 1.6 * cm, 1.0 * cm, f"Сторінка {doc.page}")
    canvas.drawString(1.6 * cm, 1.0 * cm, "Luxury Resort")
    canvas.restoreState()


def write_doc(filename_base: str, markdown: str):
    md_path = ROOT / f"{filename_base}.md"
    pdf_path = ROOT / f"{filename_base}.pdf"
    md_path.write_text(markdown, encoding="utf-8")

    font, bold = register_fonts()
    styles = build_styles(font, bold)
    story = markdown_to_story(markdown, styles)
    doc = SimpleDocTemplate(
        str(pdf_path),
        pagesize=A4,
        rightMargin=1.6 * cm,
        leftMargin=1.6 * cm,
        topMargin=1.5 * cm,
        bottomMargin=1.6 * cm,
        title=filename_base,
        author="Luxury Resort",
    )
    doc.build(story, onFirstPage=footer, onLaterPages=footer)


