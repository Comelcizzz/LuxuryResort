def register_fonts() -> tuple[str, str]:
    normal = "ArialUnicode"
    bold = "ArialUnicodeBold"
    pdfmetrics.registerFont(TTFont(normal, str(FONT_PATH)))
    if FONT_BOLD_PATH.exists():
        pdfmetrics.registerFont(TTFont(bold, str(FONT_BOLD_PATH)))
    else:
        bold = normal
    return normal, bold


